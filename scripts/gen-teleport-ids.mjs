// One-off generator for src/sync/teleportItemIds.js.
// Builds TELEPORT_ITEM_IDS (item id -> our teleport id) by name-matching a COMPLETE
// OSRS item dump (incl. untradeables + every charge/variant) — the tradeable prices
// API is insufficient. Run manually when item data changes; the OUTPUT is committed.
//
// Usage:
//   curl -s https://raw.githubusercontent.com/0xNeffarion/osrsreboxed-db/master/docs/items-summary.json -o /tmp/items-summary.json
//   node scripts/gen-teleport-ids.mjs /tmp/items-summary.json > src/sync/teleportItemIds.js
import { readFileSync } from "node:fs";

const dump = JSON.parse(readFileSync(process.argv[2] || "/tmp/items-summary.json", "utf8"));

// teleportId -> predicate(lowercasedName) deciding whether an item grants that teleport.
const FAMILIES = {
  skillsNecklace: n => n.startsWith("skills necklace"),
  combatBracelet: n => n.startsWith("combat bracelet"),
  gamesNecklace: n => n.startsWith("games necklace"),
  ringOfWealth: n => n.startsWith("ring of wealth"), // incl (i) imbued, (1)-(5)
  amuletOfGlory: n => n.startsWith("amulet of glory") || n === "amulet of eternal glory",
  necklaceOfPassage: n => n.startsWith("necklace of passage"),
  slayerRing: n => n.startsWith("slayer ring"), // incl (eternal)
  ringOfDueling: n => n.startsWith("ring of dueling"),
  ringOfElements: n => n.startsWith("ring of the elements"),
  xericTalisman: n => n.startsWith("xeric's talisman"),
  royalSeedPod: n => n === "royal seed pod",
  teleportCrystal: n => n.startsWith("teleport crystal") || n === "crystal teleport seed" || n === "enhanced crystal teleport seed",
  stonyBasalt: n => n === "stony basalt",
  icyBasalt: n => n === "icy basalt",
  quetzalWhistle: n => n.includes("quetzal whistle"), // basic / perfected / enhanced
  digsitePendant: n => n.startsWith("digsite pendant"),
  kharedstsMemoirs: n => n === "kharedst's memoirs" || n === "book of the dead",
  pendantOfAtes: n => n.startsWith("pendant of 'ates") || n.startsWith("pendant of ates"),
  questPointCape: n => n.startsWith("quest point cape"), // incl (t)
  ectophial: n => n === "ectophial",
  farmingCape: n => n.startsWith("farming cape"), // bonus; reliable signal is level 99
};

const map = {}; // itemId -> teleportId
const byFamily = {}; // teleportId -> [{id,name}]
for (const v of Object.values(dump)) {
  if (!v || typeof v.name !== "string") continue;
  const lname = v.name.toLowerCase().trim();
  for (const [tpId, pred] of Object.entries(FAMILIES)) {
    if (pred(lname)) {
      map[v.id] = tpId;
      (byFamily[tpId] ||= []).push({ id: v.id, name: v.name });
      break;
    }
  }
}

// Emit sorted-by-id object, grouped with comments per family for readability.
const lines = [];
lines.push("// AUTO-GENERATED by scripts/gen-teleport-ids.mjs — do not edit by hand.");
lines.push("// Maps OSRS item id -> our teleport id (every charge/variant maps to the same id,");
lines.push("// so owning ANY counts). Source: osrsreboxed items dump (untradeables + variants).");
lines.push("");
lines.push("export const TELEPORT_ITEM_IDS = {");
for (const tpId of Object.keys(FAMILIES)) {
  const items = (byFamily[tpId] || []).sort((a, b) => a.id - b.id);
  if (!items.length) { lines.push(`  // !! NO ITEMS MATCHED for ${tpId}`); continue; }
  lines.push(`  // ${tpId}: ${items.map(i => i.name).join(", ")}`);
  lines.push("  " + items.map(i => `${i.id}: "${tpId}"`).join(", ") + ",");
}
lines.push("};");
lines.push("");
lines.push("// Pure. arrays = flat [id,qty,id,qty,...] (bank, inventory, equipment, rune_pouch,");
lines.push("// seed_vault). Returns a Set of our teleport ids present with qty>0. Never throws.");
lines.push("export function ownedTeleportIdsFromItems(...arrays) {");
lines.push("  const owned = new Set();");
lines.push("  for (const arr of arrays) {");
lines.push("    if (!Array.isArray(arr)) continue;");
lines.push("    for (let i = 0; i + 1 < arr.length; i += 2) {");
lines.push("      const id = Number(arr[i]);");
lines.push("      const qty = Number(arr[i + 1]);");
lines.push("      if (!Number.isFinite(id) || !Number.isFinite(qty) || qty <= 0) continue;");
lines.push("      const tp = TELEPORT_ITEM_IDS[id];");
lines.push("      if (tp) owned.add(tp);");
lines.push("    }");
lines.push("  }");
lines.push("  return owned;");
lines.push("}");
process.stdout.write(lines.join("\n") + "\n");
